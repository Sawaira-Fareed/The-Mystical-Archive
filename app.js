function renderStoryGrid(stories) {
    const grid = DOM.storyGrid;
    grid.innerHTML = '';
    if (!stories || stories.length === 0) {
        grid.innerHTML = `
            <div style="grid-column:1/-1;text-align:center;padding:60px 20px;color:rgba(155,139,114,0.5);font-family:'Lora',serif;">
                No tales match your search.
            </div>
        `;
        return;
    }
    stories.forEach(s => {
        grid.appendChild(renderStoryCard(s, grid));
    });
}

