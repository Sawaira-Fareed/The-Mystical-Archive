function getCultures() {
    return [...new Set(STORIES.map(s => s.culture))];
}

function getThemes() {
    return [...new Set(STORIES.flatMap(s => s.themes))];
}

function toggleBookmark(id) {
    if (state.bookmarks.has(id)) {
        state.bookmarks.delete(id);
    } else {
        state.bookmarks.add(id);
    }
    saveBookmarks();
    updateUI();
}

function isBookmarked(id) {
    return state.bookmarks.has(id);
}

function saveBookmarks() {
    try {
        localStorage.setItem('mystical_archive_bookmarks', JSON.stringify([...state.bookmarks]));
    } catch (e) {}
}

function loadBookmarks() {
    try {
        const data = localStorage.getItem('mystical_archive_bookmarks');
        if (data) {
            state.bookmarks = new Set(JSON.parse(data));
        }
    } catch (e) {}
}

function filterStories() {
    let result = [...state.stories];
    const q = state.searchQuery.toLowerCase().trim();
    if (q) {
        result = result.filter(s =>
            s.title.toLowerCase().includes(q) ||
            s.culture.toLowerCase().includes(q) ||
            s.themes.some(t => t.toLowerCase().includes(q))
        );
    }
    if (state.selectedCulture) {
        result = result.filter(s => s.culture === state.selectedCulture);
    }
    if (state.selectedTheme) {
        result = result.filter(s => s.themes.includes(state.selectedTheme));
    }
    state.filtered = result;
    return result;
}

function getRandomStory() {
    const pool = state.filtered.length > 0 ? state.filtered : state.stories;
    return pool[Math.floor(Math.random() * pool.length)];
}

