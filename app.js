function showPage(page) {
    console.log('🔄 Navigating to:', page);
    state.currentPage = page;

    const heroSection = document.querySelector('.hero');
    const exploreSection = document.querySelector('.explore');
    const submitSection = DOM.submitPage;
    const favoritesSection = DOM.favoritesPage;

    // HIDE EVERYTHING
    if (heroSection) heroSection.style.display = 'none';
    if (exploreSection) exploreSection.style.display = 'none';
    if (submitSection) submitSection.style.display = 'none';
    if (favoritesSection) favoritesSection.style.display = 'none';

    // SHOW ONLY SELECTED
    if (page === 'home') {
        if (heroSection) heroSection.style.display = 'flex';
        if (exploreSection) exploreSection.style.display = 'block';
        applyFilters();
    } else if (page === 'submit') {
        if (submitSection) submitSection.style.display = 'block';
    } else if (page === 'favorites') {
        if (favoritesSection) favoritesSection.style.display = 'block';
        renderFavorites();
    }

    // Update active nav links
    qsa('.nav-link').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.page === page);
    });

    window.scrollTo({ top: 0, behavior: 'smooth' });
}

