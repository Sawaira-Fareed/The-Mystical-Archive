const PLACEHOLDER_SVG = `data:image/svg+xml,%3Csvg xmlns='http://www.w3.org/2000/svg' viewBox='0 0 400 300'%3E%3Crect width='400' height='300' fill='%231A1614'/%3E%3Ctext x='200' y='140' font-family='serif' font-size='48' fill='%23B8913D' text-anchor='middle' opacity='0.3'%3E📜%3C/text%3E%3Ctext x='200' y='180' font-family='serif' font-size='14' fill='%239A8F7E' text-anchor='middle' opacity='0.4'%3EStory Illustration%3C/text%3E%3C/svg%3E`;

/**
 * Handles image loading errors gracefully
 * @param {HTMLImageElement} img - The image element that failed
 */
function handleImageError(img) {
    if (!img) return;
    
    // Prevent infinite loop
    img.onerror = null;
    
    // Set fallback SVG placeholder
    img.src = PLACEHOLDER_SVG;
    img.alt = img.alt || '📜 Story illustration';
    img.classList.add('error');
    
    // Log for debugging (optional)
    console.warn('📸 Image failed to load:', img.src);
}

/**
 * Safely creates an image element with error handling
 */
function createSafeImage(src, alt, className = '') {
    const img = document.createElement('img');
    img.src = src || PLACEHOLDER_SVG;
    img.alt = alt || 'Story illustration';
    img.className = className;
    img.loading = 'lazy';
    img.onerror = function() { handleImageError(this); };
    return img;
}

/**
 * Generates a fallback icon overlay for the image container
 */
function createFallbackIcon() {
    const span = document.createElement('span');
    span.className = 'fallback-icon';
    span.textContent = '📜';
    return span;
}