function handleSubmit(e) {
    e.preventDefault();
    const title = $('sTitle').value.trim();
    const culture = $('sCulture').value.trim();
    const era = $('sEra').value.trim();
    const themes = $('sThemes').value.split(',').map(s => s.trim()).filter(Boolean);
    const excerpt = $('sExcerpt').value.trim();
    const content = $('sContent').value.trim();

    if (!title) { alert('Please enter a title.'); return; }
    if (!culture) { alert('Please specify the cultural origin.'); return; }
    if (!excerpt) { alert('Please provide a brief excerpt.'); return; }
    if (!content) { alert('Please write the full tale.'); return; }

    const newStory = {
        id: Date.now(),
        title,
        culture,
        era: era || 'Unknown Era',
        themes: themes.length ? themes : ['Untold'],
        excerpt,
        content,
        image: 'https://images.unsplash.com/photo-1519681393784-d120267933ba?w=600&h=400&fit=crop&auto=format',
        readTime: '5 min read'
    };

    const pending = JSON.parse(localStorage.getItem('mystical_archive_pending') || '[]');
    pending.push(newStory);
    localStorage.setItem('mystical_archive_pending', JSON.stringify(pending));

    DOM.submitForm.style.display = 'none';
    DOM.submitSuccess.style.display = 'block';
}

