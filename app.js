function openModal(story) {
    state.currentStory = story;
    DOM.modalImage.src = story.image;
    DOM.modalImage.alt = story.title;
    DOM.modalCulture.textContent = story.culture;
    DOM.modalEra.textContent = story.era;
    DOM.modalTitle.textContent = story.title;
      const modalImg = createSafeImage(
        story.image,
        story.title,
        'modal-image'
    );
    
    // Replace the existing modal image
    const imageContainer = DOM.modal.querySelector('.modal-image');
    if (imageContainer) {
        imageContainer.replaceWith(modalImg);
    } else {
        // If no image container exists, create one
        const wrapper = document.createElement('div');
        wrapper.className = 'img-wrap modal-img-wrap';
        wrapper.appendChild(modalImg);
        DOM.modal.insertBefore(wrapper, DOM.modal.querySelector('.modal-body'));
    }

    const saved = isBookmarked(story.id);
    DOM.modalSaveBtn.textContent = saved ? '♥ Preserved' : '♡ Preserve Tale';
    DOM.modalSaveBtn.className = `save-btn ${saved ? 'saved' : ''}`;
    DOM.modalSaveBtn.onclick = () => {
        toggleBookmark(story.id);
        const isNowSaved = isBookmarked(story.id);
        DOM.modalSaveBtn.textContent = isNowSaved ? '♥ Preserved' : '♡ Preserve Tale';
        DOM.modalSaveBtn.className = `save-btn ${isNowSaved ? 'saved' : ''}`;
        applyFilters();
        renderFavorites();
    };

    DOM.modalContent.innerHTML = story.content.split('\n\n').map(p =>
        `<p>${p.trim()}</p>`
    ).join('');

    DOM.modalTags.innerHTML = story.themes.map(t =>
        `<span>${t}</span>`
    ).join('');

    DOM.modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    DOM.modal.classList.remove('active');
    document.body.style.overflow = '';
    state.currentStory = null;
}

